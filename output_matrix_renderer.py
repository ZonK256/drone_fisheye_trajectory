import numpy as np
import matplotlib.pyplot as plt
import os

vectors = ["-1.-1", "-1.0", "-1.1", "0.-1", "random", "0.1", "1.-1", "1.0", "1.1"]

matrix_files = [f for f in os.listdir() if "output_matrix" in f]

for vector in vectors:
    # find all files with current vector
    matricies = [f for f in matrix_files if vector in f]

    if len(matricies) == 0:
        print(f"Missing matrix for {vector} vector")
        continue

    matrix = np.load(matricies[0])
    for m in matricies[1:]:
        matrix += np.load(m)

    # normalize
    matrix = matrix / matrix.max()
    # rotate matrix 90 degrees, so it looks like in the simulation
    matrix = np.rot90(matrix, k=1)

    Rpix = matrix.shape[0] // 2

    plt.figure()
    plt.title(f"{vector.replace('_', ' ')} vector (Rpix={Rpix})")
    plt.axis("equal")
    plt.imshow(matrix, cmap="hot")
    plt.colorbar()
    plt.show()


